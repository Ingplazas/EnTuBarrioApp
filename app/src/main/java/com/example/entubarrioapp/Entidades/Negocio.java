package com.example.entubarrioapp.Entidades;

public class Negocio {

    private String nombreN;
    private String direccionN;
    private String telefonoN;
    private String pagWebN;
    private String passwordN;

    public Negocio() {
    }

    public String getNombreN() {
        return nombreN;
    }

    public void setNombreN(String nombreN) {
        this.nombreN = nombreN;
    }

    public String getDireccionN() {
        return direccionN;
    }

    public void setDireccionN(String direccionN) {
        this.direccionN = direccionN;
    }

    public String getTelefonoN() {
        return telefonoN;
    }

    public void setTelefonoN(String telefonoN) {
        this.telefonoN = telefonoN;
    }

    public String getPagWebN() {
        return pagWebN;
    }

    public void setPagWebN(String pagWebN) {
        this.pagWebN = pagWebN;
    }

    public String getPasswordN() {
        return passwordN;
    }

    public void setPasswordN(String passwordN) {
        this.passwordN = passwordN;
    }
}
